package com.nandu.Ward;

import java.sql.Timestamp;


public class BedAssignment {
	
	private String bedId;
	private String patientId;
	private Timestamp admitDate;
	private Timestamp expectedReleaseDate;
	public String getBedId() {
		return bedId;
	}
	public void setBedId(String bedId) {
		this.bedId = bedId;
	}
	public String getPatientId() {
		return patientId;
	}
	public void setPatientId(String patientId) {
		this.patientId = patientId;
	}
	public Timestamp getAdmitDate() {
		return admitDate;
	}
	public void setAdmitDate(Timestamp admitDate) {
		this.admitDate = admitDate;
	}
	public Timestamp getExpectedReleaseDate() {
		return expectedReleaseDate;
	}
	public void setExpectedReleaseDate(Timestamp expectedReleaseDate) {
		this.expectedReleaseDate = expectedReleaseDate;
	}

}
