package com.ensulin.models.dto;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="med_drugstore_supplier")
public class Supplier {
	private String  supplier_name;
	@Id
	@Column(name="supplier_id")
	private long   supplier_id;
	@Column(name="supplier_description")
	private String  supplier_description;	
	@Column(name="supplier_rating")
	private int     supplier_rating;
	public String getSupplier_name() {
		return supplier_name;
	}
	public void setSupplier_name(String supplier_name) {
		this.supplier_name = supplier_name;
	}
	public long getSupplier_id() {
		return supplier_id;
	}
	public void setSupplier_id(long supplier_id) {
		this.supplier_id = supplier_id;
	}
	public String getSupplier_description() {
		return supplier_description;
	}
	public void setSupplier_description(String supplier_description) {
		this.supplier_description = supplier_description;
	}
	public int getSupplier_rating() {
		return supplier_rating;
	}
	public void setSupplier_rating(int supplier_rating) {
		this.supplier_rating = supplier_rating;
	}

	
	
}
